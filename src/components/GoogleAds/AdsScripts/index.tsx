export function getAdsScriptByPage(page: string) {
  switch (page) {
    case 'home':
      return homeAdvertisingScript;

    case 'bettingTips':
      return bettingTipsAdvertisingScript;

    case 'category':
      return categoryAdvertisingScript;

    case 'postList':
      return postListAdvertisingScript;

    case 'post':
      return postAdvertisingScript;

    case 'news':
      return newsAdvertisingScript;

    case 'casino':
      return casinoAdvertisingScript;

    default:
      return '';
  }
}

export function runSlotScript(slot: string) {
  Function(`googletag.cmd.push(function() { googletag.display(${slot}); });`)();
}

export function runInlineAdsScripts(pageType: string) {
  Function(getAdsScriptByPage(pageType))();

  if (pageType === 'home') {
    runSlotScript('rasgado_2');
    runSlotScript('vertical_1');
    runSlotScript('vertical_2');
    runSlotScript('rodapefixo');
  }

  if (pageType === 'bettingTips') {
    runSlotScript('vertical_1');
    runSlotScript('rasgado_1');
    runSlotScript('rasgado_mobile_1');
    runSlotScript('rodapefixo');
  }

  if (pageType === 'category') {
    runSlotScript('rasgado_1');
    runSlotScript('rodapefixo');
  }

  if (pageType === 'postList') {
    runSlotScript('vertical_1');
    runSlotScript('rodapefixo');
  }

  if (pageType === 'post') {
    runSlotScript('quadrado_1');
    runSlotScript('quadrado_2');
    runSlotScript('quadrado_3');
    runSlotScript('quadrado_4');
    runSlotScript('rodapefixo');
  }

  if (pageType === 'news') {
    runSlotScript('quadrado_1');
    runSlotScript('quadrado_2');
    runSlotScript('quadrado_3');
    runSlotScript('quadrado_4');
    runSlotScript('quadrado_6');
    runSlotScript('rodapefixo');
    runSlotScript('rasgado_1');
    runSlotScript('rasgado_mobile_1');
    runSlotScript('rasgado_mobile_2');
  }

  if (pageType === 'casino') {
    runSlotScript('vertical_1');
    runSlotScript('rodapefixo');
  }
}
